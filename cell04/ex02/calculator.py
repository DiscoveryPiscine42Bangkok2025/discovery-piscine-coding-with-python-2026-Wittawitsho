#!/usr/bin/env python3

first_number = input("Give me the first number: ")
second_number = input("Give me the second number: ")

a = int(first_number)
b = int(second_number)

print("Thank you!")
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} / {b} = {a // b}")
print(f"{a} * {b} = {a * b}")
